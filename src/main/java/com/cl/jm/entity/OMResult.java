package com.cl.jm.entity;

public class OMResult {
	
	private OMInfo data;
	
	private String message;
	
	private int status;

	public OMInfo getData() {
		return data;
	}

	public void setData(OMInfo data) {
		this.data = data;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
	
	
}
